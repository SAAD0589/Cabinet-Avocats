     *
     * @var string[]
     */
    protected $fillable = [
        'name',
        'LastName',
        'nom_Agence',
        'RC',
        'ICE',
        'cin',
        'passport',
        'images',
        'email' ,
        'role',
        'isDeleted',
        'password',
        'type_clt',
        'Tel_Portable',
        'Tel_Fix',
        'Fax',
        'status',
        'city',
        'Adr',
        'Adjectifs'
        
    ];


    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array
     */
    protected $hidden = [
        'password',
        'remember_token',
        'two_factor_recovery_codes',
        'two_factor_secret',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    /**
     * The accessors to append to the model's array form.
     *
     * @var array
